public class RelationalOperator {


	public static void main(String[] args) {
		long fNumber=100;
		long sNumber=200;

		// >

		System.out.println(fNumber>sNumber);

		// <

		System.out.println(fNumber<sNumber);

		// >=
		System.out.println(fNumber>=sNumber);

		// >=
		System.out.println(fNumber<=sNumber);

		// Note in above sentence we are applying two condition
		// one is < and second is = if any of these two condition
		// fullfil we will get true result else we will get false 
		
		// hw--  take  two number where <= will be true 
		// hw take two float number where >= will be true
	}
}